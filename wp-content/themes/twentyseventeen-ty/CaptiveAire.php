    $(function () {
        //Width-based swiper menus:
        var menuWidth = 0
        var productsWidth = 0

        var imgLoad = imagesLoaded('.Menu img');
        imgLoad.on('always', function () {

            $(".nav-swiper-container").each(function () {
                menuID = $(this).attr("id");
                menuWidth = 0;
                productsWidth = 0;

                menuWidth = $(this).width();

                $(this).find("> .nav-swiper-wrapper > .swiper-slide").each(function () {
                    productsWidth += $(this).width();
                });

                if (productsWidth > menuWidth) {
                    var mySwiper = new Swiper('#' + menuID, {
                        wrapperClass: 'nav-swiper-wrapper',
                        slidesPerView: 'auto',
                        freeMode: true
                    })
                }
            });

            $(".MenuWrap .Menu, .MenuWrap .SubMenu, .MenuWrap .SubMenuContent").hide();
            $(".MenuWrap").css({ visibility: "visible" });
        });

        $("#Ventilation, .Nav .HasMenu a, .MenuWrap .Menu a").click(function () {
            if (!this.hasAttribute("href")) {
                var self = $(this);
                var thisId = self.attr("id");
                var target = $("#" + thisId + "_Menu");
                var parentClass = target.parent().attr("class");

                if (parentClass == "Menu") {
                    if ($(".Menu").is(":hidden")) {
                        self.addClass("Selected");
                        target.fadeIn(75);
                        $(".Menu").slideDown(180);

                    } else {
                        if (target.is(":visible")) {
                            $(".Selected").removeClass("Selected");
                            $(".MenuContent:visible").fadeOut(100);
                            $(".SubMenuContent:visible").fadeOut(100);
                            $(".SubMenu").slideUp(120);
                            $(".Menu").slideUp(120);

                        } else {
                            $(".Selected").removeClass("Selected");
                            if ($(".SubMenuContent").is(":visible")) {
                                $(".SubMenuContent:visible").fadeOut(100);
                                $(".SubMenu").slideUp(120);
                            }
                            self.addClass("Selected");
                            $(".MenuContent:visible").fadeOut(50, function () {
                                target.fadeIn(50);
                            });
                        }
                    }
                }

                if (parentClass == "SubMenu") {
                    if ($(".SubMenu").is(":visible")) {
                        $(".Menu .Selected").removeClass("Selected");
                        if (target.is(":visible")) {
                            $(".SubMenu").slideUp(120);
                            $(".SubMenuContent:visible").fadeOut(100);

                        } else {
                            $(".SubMenuContent:visible").fadeOut(50, function () {
                                self.addClass("Selected");
                                target.fadeIn(50);
                            });
                        }

                    } else {
                        self.addClass("Selected");
                        target.fadeIn(75);
                        $(".SubMenu").slideDown(180);

                    }
                }
            }
        });

